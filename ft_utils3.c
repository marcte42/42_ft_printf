int		ft_isflag(char c)
{
	return (c == '-' || c == '+' || c == ' ' || c == '0' || c == '#');
}

int		ft_istype(char c)
{
	return (c == 'c' || c == 's' || c == 'p' || c == 'd' || \
			c == 'i' || c == 'u' || c == 'c' || c == 'c' || \
			c == 'n' || c == 'f' || c == 'g' || c == 'e' || c == '%');
}
